idade = int(input('Digite sua idade: '))

if idade >= 5 and idade <= 10:
    print('Você faz parte da categoria infantil')
if idade >= 11 and idade <= 15:
    print('Você faz parte da categoria juvenil')
if idade >= 16 and idade <= 20:
    print('Você faz parte da categoria júnior')
if idade >= 21 and idade <= 25:
    print('Você faz parte da categoria profissional')
    
    
            
